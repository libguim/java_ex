public class ex04 {

	public static void main(String[] args) {
		
		for(int i=1; i<10; i++) {
			
			for(int j=1; j<10; j++) {
				
				System.out.println(i + "*" + j + "=" + i*j);
			} //for2
			
			System.out.print('\t');
			System.out.println();
			
		}//for1
			
	}

}
